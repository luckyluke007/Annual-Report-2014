<h1 style="margin-top:15px;">Chancellor's Message</h1>
<p>Dear Friends of UC Santa Cruz,</p>
<p>This spring, an unprecedented number of alumni and friends converged on campus for a very special edition of our Alumni Weekend celebration.</p>
<p>They came to mark the 50th anniversary of UC Santa Cruz. Those who took part saw a campus that is looking toward the future while holding on to the spirit of '65.</p>
<p>Bold inquiry, cross-disciplinary learning, and risk-taking remain strong up here in the redwoods—thanks to your generous support.</p>
<p>Goals that once seemed distant are getting closer now, including the fundraising push to revive our beautiful Quarry Amphitheater. The Campaign for UC Santa Cruz is also supporting students, faculty, and programs across campus, and highlighting areas of extraordinary impact including genomics, coastal sustainability, data science, and the arts.</p>
<p>Thank you for supporting this remarkable place and being part of our UC Santa Cruz family. Here's to the next 50 years!</p>
<p>Sincerely,</p>
