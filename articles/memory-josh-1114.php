<h2>In memory of Josh</h2>
</div>
<div class="bodycontent">
  <p>The family and close friends of Josh Alper are sustaining the memory of this alumnus, beloved staff member, and avid bicyclist with the Joshua Alper-Marines Foundation, which will help work-study students at McHenry Library and give a boost to UC Santa Cruz cycling club members.<br />
    <br />
    Alper, who worked at McHenry Library for eight years, was killed in 2013 while biking on Highway 1 north of Santa Cruz.<br />
    <br />
    Known for his humor, kindness, and generosity, Alper (Stevenson '98, literature) began work in the University Library in 2005. He was also a musician and a budding philanthropist.<br />
    <br />
    Alper's parents, Ted and Marsha Alper, and a group of his friends have come forward with a large contribution to establish the Joshua Alper-Marines Foundation in his memory.<br />
    <br />
    Marsha Alper said her son was &quot;a citizen of UC Santa Cruz, and Santa Cruz in general. You couldn't walk down the street without someone stopping him. He was very well loved.&quot;<br />
    <br />
    The fund will be geared especially toward Stevenson work-study students at McHenry, though Alper's parents said it would benefit students regardless of their college affiliation.</p>
