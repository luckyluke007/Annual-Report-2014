                <div class="bodycontent">
                    <p>Generous donors helped sociology graduate student Jimiliz Valiente-Neighbours fulfill her dreams while preserving a piece of history that would have been lost forever.</p>
                    <p>Donor gifts supported both the UC President’s Dissertation Year Fellowship and the Eugene Cota-Robles Fellowship, which funded Valiente- Neighbours’s dissertation writing and travel to the Philippines.</p>
                    <p>There, she interviewed former soldiers, many of them in their 90s, who fought alongside Americans against the Japanese during WWII.</p>
                    <p>The men shared stories about saluting the same flag and even marching side-by-side during the infamous Bataan Death March of 1942.</p>
                    <p>The stories of those Filipino men and their loyalty to the U.S. will live on as part of Valiente-Neighbours’s doctoral dissertation in the Sociology Department at UC Santa Cruz.</p>
                    <p>“I want to tell these veterans’ stories so other people will know about their lives,” said Valiente- Neighbours, who was born in Dagupan City in the Philippines. “But I could not have embarked on research about this important part of our history without the help of donors who make graduate research possible.”</p>
