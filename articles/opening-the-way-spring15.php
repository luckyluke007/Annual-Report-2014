<h2>Opening the way</h2>
</div>
<div class="bodycontent">
  <p><strong>Growing up in a troubled home in East San Jose, Brandon Vi would often find reasons to stay at school.</strong></p>
  <p>He would spend hours in the library combing through books on science, technology, and transportation. He also volunteered in an after-hours program, sometimes staying so late he'd miss the last bus and have to walk 45 minutes home.</p>
  <p>Today, Vi, 20 (Crown '17, physics and computer science), is working with a team to implement a mobile app for the Santa Cruz Metropolitan Transit District that may eventually help riders like himself track their bus's arrival in real time.</p>
  <p>His work is part of an internship he hopes will open the way for a career as a transportation planner or highway engineer. Without an Alumni Association scholarship, however, the transit district internship might not have been possible.</p>
  <p>Instead of finding a minimum-wage job to make ends met, Vi—who also is employed at a computer lab on campus—was able to pursue work that &quot;not only helps me develop personally, but also allows me to give back to the community,&quot; he said.</p>
  <p>According to Vi, all signs pointed to him not graduating from high school—let alone going on to college. His parents were divorced, their income was low, he lived in a gang-infected neighborhood, and his home life was complicated, he said.<br />
    <br />
    &quot;I had to take care of myself a lot, and I didn't go to a really good high school,&quot; he said. But thanks to inspiring teachers and friends who encouraged him to pursue his passion for science and technology, Vi was accepted to several UCs. He chose Santa Cruz for its coastal beauty and the opportunities it offered.<br />
    <br />
    The praise he received from others for his volunteering also gave him confidence to believe in himself, to never give up, and to beat the odds, he said.<br />
    <br />
    &quot;I also believe that persistence, giving 100 percent all the time, and taking risks is very important,&quot; he said. &quot;What's the worst that can happen to me?&quot;</p>
