<h2>Paul Hall: Why do I give to UC Santa Cruz?</h2>
</div>
<div class="bodycontent">
  <p>Why do I give to UC Santa Cruz?<br />
    A word with Paul J. Hall (Merrill '72)<br />
    <br />
    I give because I got a fantastic undergraduate education here, including great mentoring from brilliant professors who remain my friends to this day. My years here sharpened my thinking, taught me the value of interdisciplinary learning, and set me on my way in life. Now more than ever, public universities need our time, our commitment, and  our generous gifts to maintain their level of excellence.<br />
    <br />
    I gave to McHenry Library because I was lucky enough to be here when Dean McHenry was the visionary founding chancellor—and the campus' excellent library bears his name.<br />
    <br />
    I made a gift to endow a coffee bar at McHenry Library, which seems only right, considering I am a practicing lawyer who puts in long hours; you might say I am powered by espresso! But in all seriousness, the campus needed café facilities in a place where students meet to study. That worked out so well that I decided to endow a coffee bar at the Science and Engineering Library.<br />
    <br />
    Giving back to UC Santa Cruz feels great, whether your contribution goes to faculty retention funds, the Smith Renaissance Society, the mock trial team, restoration of the Quarry, or any other program that reflects your passions and values. You will like the way giving makes you feel—making a difference at a place you love, a place that deserves your support.<br />
    <br />
    The ongoing Campaign for UC Santa Cruz, and the campus' 50th anniversary in 2015, will add a celebratory feeling to your gifts.<br />
    <br />
    When your parents or grandparents have a 50th wedding anniversary, you throw a heck of a big party. We want people to think of UC Santa Cruz as an extended family,to enjoy a lifetime relationship with it, and to join in that 50th anniversary celebration.<br />
    <br />
    As president of the UC Santa Cruz Foundation, I am pleased to share the stories included here—stories of selfless giving, vision, and generosity.<br />
    <br />
    This past fiscal year, donors stepped forward to give an unprecedented $50 million. In these pages, you'll read about the stories and people behind the gifts, including:</p>
  <ul>
    <li> The beloved McHenry Library staff member whose parents and friends have commemorated him with a foundation that bears his name.<br />
      <br />
    </li>
    <li>The student who is benefiting from the generosity of her fellow undergraduates, while planning to &quot;pay it forward.&quot;<br />
      <br />
    </li>
    <li> The philanthropic husband-and-wife team who want to help lower-income students thrive in the arts.<br />
      <br />
    </li>
    <li>The campus' record-setting 2013-14 fiscal year, made possible by generous donors who believe in our vision..</li>
    </ul>
  <p>These are stories of selfless giving—gifts of the heart, of the soul, of the mind, that will live on forever in generations of students to come. These gifts are propelling UC Santa Cruz forward, to its next level of achievement.  Join us in that endeavor.<br />
    <br />
    Paul J. Hall (Merrill, '72)<br />
    President, UC Santa Cruz Foundation<br />
    Partner, DLA Piper LLP (US)</p>
