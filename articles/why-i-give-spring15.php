                    <h2>Stephanie Harkness: Why do I give to UC Santa Cruz?</h2>
                </div>
                <div class="bodycontent">
                    <p>
                        <strong>A word with Stephanie Harkness, 
                            <em>UC Santa Cruz Foundation Board member</em>
                        </strong>
                    </p>
                    <p>I've discovered something important during my time as a Foundation Board member at UC Santa Cruz over the past three years—there are many ways to give to the university, just as there are many ways to become a part of the campus community.</p>
                    <p>I was a typical Gidget way back when, growing up riding the waves, spending my summers on Balboa Island, Huntington Beach, San Onofre, Laguna Beach, and, of course, Santa Cruz. For years my husband and I co-owned a successful business in Soquel and a home in Pasatiempo. It was comforting and inspiring to look out the window and see waves.</p>
                    <p>Giving to UC Santa Cruz allows us to support what inspires and fires our imaginations, whether it's groundbreaking scientific research, experiential education, or interdisciplinary learning. In my case, I want to protect the ocean that has always centered me and given me so much joy.</p>
                    <p>My husband Jack and I support the Seymour Marine Discovery Center and the Joseph M. Long Marine Laboratory because we want to ensure that the Monterey Bay Marine Sanctuary is protected for generations to come.</p>
                    <p>To do that requires stewardship and understanding. When I support UC Santa Cruz in various ways, including a planned gift I've arranged to make, I'm investing in the future. That's why I place a premium on hands-on experiences for students, whether they're post-docs or elementary school students. At the Seymour Center, children can observe a decorator crab putting pink cotton balls on its back. They can let a little hermit crab tickle their hand. If they're feeling brave, they can reach down and pet a swell shark.</p>
                    <p>As a supporter of UC Santa Cruz, I get to review proposals for grants that graduate students write to support their research. Reading their papers gives me hope. There is so much talent and curiosity out there. These hard-working scholars are traveling the world, uncovering the secrets of the ocean.</p>
                    <p>
                        <strong>&quot;Giving to UC Santa Cruz allows us to support what inspires and fires our imaginations, whether it's groundbreaking scientific research, experiential education, or interdisciplinary learning.&quot;</strong>
                    </p>
                    <p>As part of the UC Santa Cruz family, I am pleased to share with you a few stories about generosity and vision. In these pages you'll read about some of the stories and people behind the gifts, including:</p>
                    <ul>
                        <li>The third-generation Central Valley fruit and nut grower who fell in love with organic farming at UC Santa Cruz.</li>
                        <li>The successful artist and museum director who is endowing a visiting artists program on campus so future students can have life-transforming experiences.</li>
                        <li>The hard-working, high-achieving student who is benefiting from the generosity of UC Santa Cruz alumni.</li>
                        <li>The man who advocated for a generous endowment to establish the first endowed chair in storage and security at UC Santa Cruz.</li>
                    </ul>
                    <p>These are stories about pride, place, and connection—gifts that reflect individual passions and interests that have fueled the campus for a half-century. Just as the ocean gives me energy, these areas of interest inspire the donors, while making them partners in the continuing success of UC Santa Cruz.</p>
