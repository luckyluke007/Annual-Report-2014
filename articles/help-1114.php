<div class="bodycontent">
  <p>Third-generation Central Valley farmer Sharon Naraghi fell in love with UC Santa Cruz's sustainable farming program when she visited the campus's organic farm stand and signed up for a farm-to-fork dinner. </p>
  <p>While Naraghi's father mostly employed conventional farming methods, he used water-conserving irrigation techniques from his native Iran, and set aside 10 acres that were completely organic, with Persian grapes, fruits, and vegetables. </p>
  <p>&quot;My whole life was going down to the orchard,&quot; Naraghi recalled. &quot;I can still smell the produce and the soil.&quot;</p>
  <p>Naraghi, who grows almonds, pistachios, walnuts, and pomegranates, has become a passionate supporter of UC Santa Cruz's Center for Agroecology and Sustainable Food Systems (CASFS). Giving to UC Santa Cruz has helped firm up her emotional connection to Santa Cruz, where she has vacationed since childhood. It's also a way to remember her late father's interest in organic farming. </p>
  <p>&quot;I know he is happy that I'm doing as much as I can,&quot; she said.</p>
