                    <p>
                        <strong>A word with Ricky Bluthenthal, UC Santa Cruz Foundation Board member<em></em>
                        </strong>
                    </p>
                  <p>UC Santa Cruz had a big impact on my life when I was a student, and it still does. Being there helped me figure out how the world worked. You might even say it launched me.</p>
                    <p>I was very ambitious as an undergraduate—ready to go, anxious to make my mark on the world. But UC Santa Cruz provided more than just the inspiration; it also gave me the means to get where I wanted to go and helped me figure out how to have the impact I wanted.</p>
                    <p>Now, with the map I started making at UC Santa Cruz, I’ve navigated my way to a life of helping some of the people in our community who need it the most.</p>
                    <p>This is why I have always given to the university—because of what it did for me.</p>
                    <p>UC Santa Cruz gave me the opportunity to become involved in its vibrant student life. As a student leader I was involved in the creation of the campus-wide student government, the anti-apartheid divestment movement, and the adoption of an ethnic studies breadth requirement (or general education requirement).</p>
                    <p>Having that kind of impact is empowering—and the campus also gave me a chance to develop close relationships with faculty who had a strong and positive influence on me.</p>
                    <p>Those relationships inspired me, but they also had realworld implications. I studied with a visiting scholar, Roger Wilkins, and went on to work for him later on at a think tank in Washington, D.C.</p>
                    <p>At a young age, I figured out that I wanted to help disadvantaged groups. UC Santa Cruz showed me I could do that by being an engaged, thoughtful researcher—and that is what I’ve been doing ever since.</p>
                    <p>As a graduate student I helped start and run a needle exchange program in Oakland, and since then I have been conducting research on strategies to prevent the spread of HIV and HCV among vulnerable groups. I have conducted research on a wide range of public health issues that are associated with race and class disparities in health.</p>
                    <p>This kind of work arises directly from the social consciousness, the practical skills and connections I nurtured at UC Santa Cruz. In the years since I graduated, the percentage of the state’s contribution to the overall UC budget has decreased dramatically. The campus—and its excellent programs—need our support more than ever.</p>
                  <p>College is such an important time in your life, and if it goes well there are impressive dividends. It went well for me, and I’m reaping those dividends even now, and so is my wife, Nancy Berglass (Merrill ‘85, community studies), also a proud graduate of UC Santa Cruz.</p>
                  <p><em><strong>“We can see how important it is to make sure the opportunities we received as students are available for generations to come.”</strong></em></p>
                  <p>We can see how important it is to make sure the opportunities we received as students are available for generations to come.</p>
                  <p>As an alumnus who serves on the Foundation Board of Trustees, I would like to share with you these stories of support and commitment:</p>
                  <ul>
                    <li>The intellectual property lawyer who is giving back to UC Santa Cruz because of the interdisciplinary problem-solving skills it gave him.'</li>
                    <li>The hard-working graduate student who is using her funding to share a piece of history that would otherwise be lost.</li>
                    <li>The faculty member who is keeping the memory of his wife alive while helping underprivileged women make their way in STEM careers.</li>
                    <li>And the continuing legacy of our founding chancellor, Dean McHenry, whose bequest gifts support the campus he loved so well.</li>
                  </ul>
                  <p><strong>RICKY BLUTHENTHAL (MERRILL ‘86, HISTORY AND SOCIOLOGY</strong>)<br>
