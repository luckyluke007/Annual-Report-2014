                    <p>UC Santa Cruz’s founding chancellor Dean McHenry set the tone for the campus’s remarkable combination of large research university sophistication and intimate residential colleges when it opened 50 years ago.</p>
                    <p>McHenry died in 1998, and his wife, Jane McHenry, died in 2013, but their survivors are helping this visionary couple sustain their support of UC Santa Cruz long into the future.</p>
                    <p>Before their deaths, the McHenrys made arrangements for funds from their estate to support the UC Santa Cruz Foundation, with gifts that will enhance the Colleges Endowment as well as support the Arboretum, Long Marine Lab, the Farm &amp; Garden, and sports on campus.</p>
                    <p>Their sons Dean McHenry and Henry McHenry— trustees of the McHenry estate—have been overseeing the bequest.</p>
                    <p>The founding chancellor’s eldest son, Dean McHenry, is proud of what his father contributed to the campus. “He helped lay the groundwork for a marvelous institution of higher education … something that gave him great pride,” he said. “Indeed, it was the crowning achievement of his academic life.”</p>
