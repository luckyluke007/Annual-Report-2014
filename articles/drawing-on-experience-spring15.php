<h2>Drawing on Experience</h2>
</div>
<div class="bodycontent">
  <p>Jock Reynolds (Stevenson '69, psychology) will never forget the dialogues he and 14 fellow students had in visiting artist Robert Watts's &quot;Experimental Arts Workshop.&quot; They interacted with the likes of composer John Cage, father of &quot;Happenings&quot; Allan Kaprow, sculptor George Segal, and other avant-garde artists.</p>
  <p>This transformative educational experience and others were funded by a major grant from the Carnegie Foundation. The grant proposal had been prepared and submitted by sculptor Gurdon Woods, founder of UC Santa Cruz's Art Department and an inspirational mentor to the young Reynolds.</p>
  <p>Seeking to ensure that such rich educational experiences continue, Reynolds has established the Alumni Fund for Visiting Artists. This endowment will give the director of the university's Institute of the Arts and Sciences permanent funding to invite visiting artists from across the creative disciplines to campus.</p>
  <p>&quot;I hope that many alumni will make gifts to help grow this newly endowed fund,&quot; Reynolds said. &quot;It's important for it to belong in collective spirit, deed, and name to graduates from all of UC Santa Cruz's classes.&quot;</p>
