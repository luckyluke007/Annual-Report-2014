                    <p>UC Santa Cruz professor of computer science Darrell Long and his wife, Mary Katherine Long, were high school sweethearts. Long’s companion for 38 years, she was a force of nature. The only daughter of a park ranger, she lived in various communities from San Diego to Eureka.</p>
                    <p>She loved camping with her two sons and riding a motorcycle. But she was especially passionate about the education and equality of women.</p>
                  <p>“Mary felt very strongly that educated women were the foundation of a civil and just society,” Long said.</p>
                    <p>Mary Long was deeply troubled by the fact that many women from underprivileged backgrounds face barriers to education and entry into lucrative STEM careers.</p>
                    <p>Mary’s death at age 53 was a great shock to Long and the couple’s children. Soon after her death, he decided to commemorate his wife by establishing the Mary Katherine Long Endowed Scholarship Fund, which will help low-income women at UC Santa Cruz pursue engineering and science educations.</p>
                    <p>Gifts to the scholarship have reached $100,000, but supporters hope it will grow much larger.</p>
                    <p>“I want the money to go to people who really need it: the single mom, the re-entry student who deferred college to support her family, or a student from a country where women are not treated as equals,” Long said. “I want to sustain her vision for years to come.”</p>
