<div class="bodycontent">
  <p>Darren Shou of Mountain View-based security and storage leader Symantec had a simple explanation for his company's $500,000 contribution toward a $1 million endowment to establish a chair in storage and security at UC Santa Cruz.</p>
  <p>&quot;It's a virtuous cycle,&quot; said Shou, a senior director of Symantec Research Labs, who played a decisive role in making this gift happen. &quot;We support the School of Engineering so they can support the best and brightest students from around the world.&quot;</p>
  <p>Meanwhile, the chair, the first at UC Santa Cruz to be endowed with matching funds from UC's Presidential Match for Endowed Chairs, will give Symantec &quot;access to a pipeline of world-class talent.&quot;</p>
  <p>The Symantec Presidential Chair in Storage and Security at the Jack Baskin School of Engineering supports research and teaching in the engineering school's Department of Computer Science.</p>
  <p>Ethan Miller, professor of computer science and director of the Center for Research in Storage Systems at UC Santa Cruz, has been appointed as the inaugural holder of the chair.</p>
  <p>Symantec's gift is a significant contribution to the Data Science Leadership Initiative of the $300 million Campaign for UC Santa Cruz.</p>
