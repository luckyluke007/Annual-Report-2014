<h2>Nurturing diversity in the sciences</h2>
</div>
<div class="bodycontent">
  <p>Julie Packard (Crown '74, biology; M.A. '78) has a lifelong love of nature and biology. The Monterey Bay was her living lab when she was a student at UC Santa Cruz.<br />
    <br />
    Now she's giving back to the institution that nurtured her; this year she gave a $1 million gift to establish the Dean's Fund for Diversity in the Sciences, designed to support programs that will help underrepresented minority students excel in science and math.<br />
    <br />
    Packard,  executive director of the Monterey Bay Aquarium, believes it's critical for the sciences to reflect the diversity of California's population, especially with so many pivotal environmental decisions ahead. This year, 10 students have already received scholarships to a summer research program on campus thanks to a portion of her gift. <br />
    <br />
    Her gift helps underrepresented minority students excel in the sciences and mathematics.<br />
    <br />
    &quot;My philosophy is that you need to work to make a change,&quot; says Packard, who is on the board of the $6.5-billion David and Lucile Packard Foundation, which handed out $288 million in grants last year.  &quot;I think all of us need to engage.&quot;</p>
